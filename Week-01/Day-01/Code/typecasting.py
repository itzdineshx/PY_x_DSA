# Typecasting - converting one data type to another data type
"""Str(), int(), float(), bool(), list() functions are used for typecasting"""

name = "Dinesh"
age = 20.5 # age will no be in decimal
gpa = 8
is_student = True

print("My Name is " + str(name))          # string typecasting
print("My Age is " + str(int(age)))       # integer typecasting
print("My GPA is " + str(float(gpa)))     # float typecasting
print("Am I a Student? " + str(bool(is_student)))